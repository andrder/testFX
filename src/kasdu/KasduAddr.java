public class KasduAddr {
    public String nameKP;
    public int numKP;
    public int numGr;
    public int numObj;

    public KasduAddr (String nameKP, int numKP, int numGr, int numObj) {
        this.nameKP = nameKP;
        this.numKP = numKP;
        this.numGr = numGr;
        this.numObj = numObj;
    }

    @Override
    public String toString() {
        return nameKP + " " + numKP + " Gr:"+numGr + " O:" + numObj;
    }
}

